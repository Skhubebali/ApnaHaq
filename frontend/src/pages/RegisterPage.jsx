const RegisterPage = () => {
  const navigate = useNavigate();
  const login = useStore(state => state.login);
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState('');
  
  const [formData, setFormData] = useState({
    fullName: '', age: '', gender: 'Male', state: 'Karnataka', district: 'Bengaluru Urban', 
    annualIncome: '', casteCategory: 'General', religion: 'Hinduism', 
    occupation: 'Student', isStudent: false, isFarmer: false, isDisabled: false, 
    employmentStatus: 'Unemployed', maritalStatus: 'Single', 
    educationLevel: '', aadhaarNumber: '', mobileNumber: '', 
    email: '', password: '', confirmPassword: ''
  });

  const handleChange = (e) => {
    const { name, value, type, checked } = e.target;
    setFormData(prev => {
      const updated = {
        ...prev,
        [name]: type === 'checkbox' ? checked : value
      };
      if (name === 'state') {
        updated.district = statesAndDistricts[value] ? statesAndDistricts[value][0] : '';
      }
      return updated;
    });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    setLoading(true);
    setError('');
    
    if (formData.password !== formData.confirmPassword) {
      setError('Passwords do not match');
      setLoading(false);
      return;
    }
    
    const { confirmPassword, ...rest } = formData;
    const dataToSend = {
      ...rest,
      age: parseInt(formData.age),
      annualIncome: parseFloat(formData.annualIncome)
    };

    try {
      const API_URL = import.meta.env.VITE_API_URL || 'http://localhost:5000';
      const response = await axios.post(`${API_URL}/api/auth/register`, dataToSend);
      login(response.data.user, response.data.token);
      navigate('/dashboard');
    } catch (err) {
      console.error('Registration Error:', err);
      if (err.response) {
        setError(err.response.data?.error || `Server Error: ${err.response.status}`);
      } else if (err.request) {
        setError(`Network Error: Backend unreachable at ${import.meta.env.VITE_API_URL || 'localhost'}. Please check if the backend is running and the VITE_API_URL environment variable is correct in Vercel.`);
      } else {
        setError(err.message || 'Registration failed');
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="container mx-auto px-4 py-8">
      <div className="bg-white p-8 rounded-xl shadow-lg max-w-4xl mx-auto border-t-4 border-india-saffron">
        <h2 className="text-3xl font-bold text-center text-gov-blue mb-2">Citizen Registration</h2>
        <p className="text-center text-gray-500 mb-8">Create your profile to discover eligible schemes.</p>
        
        {error && <div className="bg-red-50 text-red-600 p-3 rounded mb-6 text-sm">{error}</div>}
        
        <form onSubmit={handleSubmit} className="grid grid-cols-1 md:grid-cols-2 gap-6">
          {/* Basic Details */}
          <div className="space-y-4">
            <h3 className="font-semibold text-lg text-gray-700 border-b pb-2">Basic Details</h3>
            
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">Full Name</label>
              <input type="text" name="fullName" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
            </div>
            
            <div className="grid grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Age</label>
                <input type="number" name="age" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
              </div>
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Gender</label>
                <select name="gender" onChange={handleChange} className="w-full px-3 py-2 border rounded-md">
                  <option value="Male">Male</option>
                  <option value="Female">Female</option>
                  <option value="Other">Other</option>
                </select>
              </div>
            </div>

            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">Aadhaar Number (Mock)</label>
              <input type="text" name="aadhaarNumber" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" maxLength="12" />
            </div>

            <div className="grid grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Mobile</label>
                <input type="text" name="mobileNumber" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
              </div>
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Email</label>
                <input type="email" name="email" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
              </div>
            </div>

            <div className="grid grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Password</label>
                <input type="password" name="password" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
              </div>
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Confirm Password</label>
                <input type="password" name="confirmPassword" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
              </div>
            </div>
          </div>

          {/* Demographic Details */}
          <div className="space-y-4">
            <h3 className="font-semibold text-lg text-gray-700 border-b pb-2">Demographic Profile</h3>
            
            <div className="grid grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">State</label>
                <select name="state" required onChange={handleChange} value={formData.state} className="w-full px-3 py-2 border rounded-md">
                  <option value="">Select State</option>
                  {Object.keys(statesAndDistricts).map(state => (
                    <option key={state} value={state}>{state}</option>
                  ))}
                </select>
              </div>
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">District</label>
                <select name="district" required onChange={handleChange} value={formData.district} className="w-full px-3 py-2 border rounded-md" disabled={!formData.state}>
                  <option value="">Select District</option>
                  {formData.state && statesAndDistricts[formData.state]?.map(district => (
                    <option key={district} value={district}>{district}</option>
                  ))}
                </select>
              </div>
            </div>

            <div className="grid grid-cols-2 gap-4">
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Annual Income (₹)</label>
                <input type="number" name="annualIncome" required onChange={handleChange} className="w-full px-3 py-2 border rounded-md" />
              </div>
              <div>
                <label className="block text-sm font-medium text-gray-700 mb-1">Caste Category</label>
                <select name="casteCategory" onChange={handleChange} className="w-full px-3 py-2 border rounded-md">
                  <option value="General">General</option>
                  <option value="OBC">OBC</option>
                  <option value="SC">SC</option>
                  <option value="ST">ST</option>
                </select>
              </div>
            </div>

            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">Occupation</label>
              <select name="occupation" required onChange={handleChange} value={formData.occupation} className="w-full px-3 py-2 border rounded-md">
                <option value="Student">Student</option>
                <option value="Farmer">Farmer</option>
                <option value="Business">Business</option>
                <option value="Salaried">Salaried Employee</option>
                <option value="Self-Employed">Self-Employed</option>
                <option value="Daily Wage">Daily Wage</option>
                <option value="Unemployed">Unemployed</option>
              </select>
            </div>

            <div>
              <label className="block text-sm font-medium text-gray-700 mb-1">Education Level</label>
              <select name="educationLevel" required onChange={handleChange} value={formData.educationLevel} className="w-full px-3 py-2 border rounded-md">
                <option value="">Select Education Level</option>
                <option value="No Formal Education">No Formal Education</option>
                <option value="Primary School">Primary School</option>
                <option value="High School">High School</option>
                <option value="Higher Secondary (10+2)">Higher Secondary (10+2)</option>
                <option value="Diploma">Diploma</option>
                <option value="Undergraduate">Undergraduate</option>
                <option value="Postgraduate">Postgraduate</option>
                <option value="Doctorate">Doctorate</option>
              </select>
            </div>

            <div className="flex flex-wrap gap-4 mt-4">
              <label className="flex items-center space-x-2">
                <input type="checkbox" name="isStudent" onChange={handleChange} className="rounded text-gov-blue" />
                <span className="text-sm">Student</span>
              </label>
              <label className="flex items-center space-x-2">
                <input type="checkbox" name="isFarmer" onChange={handleChange} className="rounded text-gov-blue" />
                <span className="text-sm">Farmer</span>
              </label>
              <label className="flex items-center space-x-2">
                <input type="checkbox" name="isDisabled" onChange={handleChange} className="rounded text-gov-blue" />
                <span className="text-sm">Disabled Person</span>
              </label>
            </div>
          </div>

          <div className="col-span-1 md:col-span-2 mt-6">
            <button 
              type="submit" 
              disabled={loading}
              className="w-full bg-india-green text-white py-3 px-4 rounded-md hover:bg-green-700 transition-colors font-bold text-lg disabled:opacity-50"
            >
              {loading ? 'Submitting...' : 'Register Profile'}
            </button>
            <p className="mt-4 text-center text-sm text-gray-600">
              Already registered? <Link to="/login" className="text-gov-blue hover:underline font-medium">Login here</Link>
            </p>
          </div>
        </form>
      </div>
    </div>
  );
};

export default RegisterPage;
